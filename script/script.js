// main nav right btn
function bgColor() {
    const letters = "0123456789ABCDEF";
    let color = "#";
    for (let i = 0; i < 6; i++) {
        color += letters[Math.floor(Math.random() * 16)];
    }
    return color;
}

    document.getElementById('main-nav-right-btn').addEventListener('click', function(){
        document.body.style.backgroundColor = bgColor();
    });